         function changeLanguage(languageCode) {
             if (languageCode === 'en') {
            window.location.reload();
            return;
        }

            const elements = document.querySelectorAll('[data-translate]');
            elements.forEach(el => {
                if (el.tagName.toLowerCase() === 'a' || el.tagName.toLowerCase() === 'span') {
                    const text = el.innerText;
                    translateText(text, languageCode, translatedText => {
                        el.innerText = translatedText;
                    });
                } else if (el.tagName.toLowerCase() === 'h3' && el.querySelector('span[data-translate]')) {
                    const textNode = el.childNodes[0].nodeValue;
                    const linkText = el.querySelector('span[data-translate]').innerText;

                    translateText(textNode, languageCode, translatedText => {
                        el.childNodes[0].nodeValue = translatedText;
                    });

                    translateText(linkText, languageCode, translatedText => {
                        el.querySelector('span[data-translate]').innerText = translatedText;
                    });
                } else {
                    const text = el.innerText || el.value;
                    translateText(text, languageCode, translatedText => {
                        if (el.tagName.toLowerCase() === 'input' || el.tagName.toLowerCase() === 'textarea') {
                            el.value = translatedText;
                        } else {
                            el.innerText = translatedText;
                        }
                    });
                }
            });
        }

        function translateText(text, targetLanguage, callback) {
            fetch(`https://translation.googleapis.com/language/translate/v2?key=${apiKey}`, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({
                    q: text,
                    target: targetLanguage
                })
            })
            .then(response => response.json())
            .then(data => {
                if (data && data.data && data.data.translations) {
                    callback(data.data.translations[0].translatedText);
                } else {
                    console.error('Translation error:', data);
                }
            })
            .catch(error => console.error('Error:', error));
        }
    </script>
    
